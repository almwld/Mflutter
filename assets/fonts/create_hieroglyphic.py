#!/usr/bin/env python3
"""
بناء خط هيلوغريفي (فرعوني) للقرآن
يحول الحروف العربية إلى رموز فرعونية
"""

# ═══════════════════════════════════════════
# جدول التحويل: عربي → هيلوغريفي
# ═══════════════════════════════════════════
ARABIC_TO_HIERO = {
    'ا': '𓀀',   # نسر (ألف)
    'ب': '𓃀',   # ساق (باء)
    'ت': '𓏏',   # رغيف (تاء)
    'ث': '𓍿',   # ثعبان (ثاء)
    'ج': '𓆓',   # ثعبان (جيم)
    'ح': '𓎛',   # فتى (حاء)
    'خ': '𓐍',   # مذبح (خاء)
    'د': '𓂧',   # يد (دال)
    'ذ': '𓆑',   # أفعى (ذال)
    'ر': '𓂋',   # فم (راء)
    'ز': '𓊃',   # مزلاج (زاي)
    'س': '𓋴',   # قماش مطوي (سين)
    'ش': '𓈙',   # بركة (شين)
    'ص': '𓊮',   # بردي (صاد)
    'ض': '𓍑',   # سوط (ضاد)
    'ط': '𓍔',   # حبل (طاء)
    'ظ': '𓊪',   # حصيرة (ظاء)
    'ع': '𓂝',   # ذراع (عين)
    'غ': '𓎼',   # حامل (غين)
    'ف': '𓆑',   # أفعى مقرن (فاء)
    'ق': '𓏘',   # تلة (قاف)
    'ك': '𓎡',   # سلة (كاف)
    'ل': '𓃭',   # أسد (لام)
    'م': '𓅓',   # بومة (ميم)
    'ن': '𓈖',   # ماء (نون)
    'ه': '𓉔',   # كوخ (هاء)
    'و': '𓅱',   # كتكوت (واو)
    'ي': '𓇌',   # برديان (ياء)
    ' ': ' ',    # مسافة
    'ة': '𓉔',   # تاء مربوطة = هاء
    'ى': '𓇌',   # ألف مقصورة = ياء
}

def arabic_to_hiero(text):
    """تحويل نص عربي إلى هيلوغريفي"""
    result = ''
    for char in text:
        # تجاهل التشكيل
        if '\u064B' <= char <= '\u0652':
            continue
        result += ARABIC_TO_HIERO.get(char, char)
    return result

# اختبار
test = "بسم الله الرحمن الرحيم"
hiero = arabic_to_hiero(test)
print(f"عربي: {test}")
print(f"فرعوني: {hiero}")
print(f"\n✅ تم — {len(ARABIC_TO_HIERO)} حرف")
