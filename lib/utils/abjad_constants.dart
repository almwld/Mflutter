  }

  /// حساب المجموع
  static int calculateTotal(String text) {
    int total = 0;
    for (int i = 0; i < text.length; i++) {
      total += getValue(text[i]);
    }
    return total;
  }

  /// تحويل الرقم إلى اسم إلهى
  static String getDivineNameByNumber(int number) {
