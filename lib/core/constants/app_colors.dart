/// فئة الألوان للعناصر الكيميائية
class ElementColors {
  ElementColors._();

  static const Color fire = AppColors.elementFire;
  static const Color water = AppColors.elementWater;
  static const Color earth = AppColors.elementEarth;
  static const Color air = AppColors.elementAir;
  static const Color spirit = AppColors.elementSpirit;

  /// الحصول على لون العنصر بالاسم
  static Color getByName(String elementName) {
    switch (elementName.toLowerCase()) {
      case 'نار':
        return fire;
      case 'ماء':
        return water;
      case 'تراب':
        return earth;
      case 'هواء':
        return air;
      case 'روح':
        return spirit;
      default:
        return AppColors.primaryNavy;
    }
  }
}

